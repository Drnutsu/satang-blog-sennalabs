import { CancelToken } from 'axios'

interface methodParams {
  url: string
  params?: object
  data?: object
  headers?: string
  cancelToken?: CancelToken | undefined
}

interface requestParams extends methodParams {
  method:
    | 'get'
    | 'GET'
    | 'delete'
    | 'DELETE'
    | 'head'
    | 'HEAD'
    | 'options'
    | 'OPTIONS'
    | 'post'
    | 'POST'
    | 'put'
    | 'PUT'
    | 'patch'
    | 'PATCH'
    | 'purge'
    | 'PURGE'
    | 'link'
    | 'LINK'
    | 'unlink'
    | 'UNLINK'
    | undefined
}

export type { methodParams, requestParams }
